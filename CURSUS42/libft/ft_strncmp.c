int	strncmp(char *s1, char *s2, size_t n)
{
	int	i;
	int	sol;

	i = 0;
	sol = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && sol == 0)
	{
		if (s1[i] != s2[i])
			sol = s1[i] - s2[i];
		i++;
	}
	return (0);
}
