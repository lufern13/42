int	ft_str_is_uppercase(char *str)
{
	int	i;
	int	sol;

	sol = 1;
	i = 0;
	while (*(str + i) != '\0' && sol == 1)
	{
		if (str[i] < 65 || str[i] > 90)
			sol = 0;
		i++;
	}
	return (sol);
}
