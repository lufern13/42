int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	sol;

	i = 0;
	sol = 0;
	while (s1[i] != '\0' && sol == 0)
	{
		if (s1[i] != s2[i])
			sol = s1[i] - s2[i];
		i++;
	}
	return (sol);
}
